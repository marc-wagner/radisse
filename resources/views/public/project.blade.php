    <p>En mai 2019, le Val’heureux compte plus de 290 partenaires. La liste complète des partenaires est actualisée de manière régulière et est accessible sur le site web du Val’heureux et sur sa page Facebook.</p>

    <p>La liste actualisée des comptoirs de change peut également être consultée sur le site.</p>

    <h2 id="communication-et-promotion-du-val-heureux">Communication et promotion du Val’heureux</h2>

    <h3>Sur internet</h3>

    <p>Le site internet <a href="https://valheureux.be">valheureux.be</a> est régulièrement mis à jour, notamment à chaque adhésion d’un nouveau partenaire.</p>

    <p>On y trouve l’actualité du Val’heureux, l’annuaire des partenaires, toute la documentation relative au fonctionnement du Val’heureux et de l’ASBL, ainsi que la charte. Il est également possible d’envoyer un message à {!! Html::mailto('info@valheureux.be') !!}.be ou via le formulaire en ligne. Les courriels envoyés à {!! Html::mailto('info@valheureux.be') !!}.be sont traités dans les 24 heures.</p>

    <p>La page Facebook du Val’heureux est également alimentée régulièrement. Elle permet de faire connaître le Val’heureux à un plus grand nombre de personnes et surtout de faire connaître l’actualité du Val’heureux et des commerçants partenaires. La page Facebook compte actuellement plus de 3000 <em>followers</em>. Une page Facebook Val’heureux pour chaque comité régional est en cours de création.</p>

    <h3>Matériel de promotion et signalétique</h3>

    <p>Différents documents et supports de promotion ont été élaborés, notamment un flyer de présentation du Val’heureux et un dépliant répertoriant tous les commerçants partenaires.</p>

    <p>Ces documents sont actualisés régulièrement et sont disponibles sur les comptoirs des commerçants partenaires et sont distribués lors d’événements.</p>

    <p>Les prestataires partenaires disposent également d’autocollants « Ici circule le Val’heureux », qu’ils peuvent apposer sur leur vitrine ou à tout autre endroit approprié.</p>

    <h2 id="partenariat-avec-la-ville-de-liege">Partenariat avec la ville de Liège</h2>

    <p>Au printemps dernier, M. Demeyer, bourgmestre de Liège, a soumis au vote du conseil communal une motion visant à soutenir le développement du Val’heureux. Grâce à cet appui officiel, de nouveaux partenariats ont vu le jour : collaboration avec le guichet de l’énergie, l’office du tourisme ou encore le commerce liégeois et diverses autres structures d’organisation d’événements en Cité ardente.</p>

    <h2 id="questions-frequemment-posees-par-les-commercants">Questions fréquemment posées par les commerçants</h2>

    <h3>Quels sont les risques financiers ?</h3>

    <p>Aucun. Chaque euro changé en Val’heureux est placé en sécurité sur un compte Triodos appelé réserve de contrepartie. Si le système devait se casser la figure, chaque commerçant ou citoyen qui détient des Val’heureux pourrait les rééchanger contre des euros.</p>

    <h3>Quel impact sur ma comptabilité ?</h3>

    <p>Le Val’heureux ne change rien à la comptabilité du commerce. D’un point de vue comptable, quand un V entre dans la caisse, c’est comme si un € entrait dans la caisse. La parité de 1 € = 1 V a été choisie pour faciliter la gestion du V et surtout pour faciliter la vie des commerçants et des producteurs partenaires. Il n’y a donc aucun souci avec la TVA ou les impôts. Il est à noter que, contrairement à certaines idées reçues, la parité 1 € = 1 V n’enlève rien à l’efficacité et aux vertus du Val’heureux.</p>

    <h3>Et si j’ai trop de Val’heureux en caisse ?</h3>

    <p>Un tel projet met du temps à se construire. Pour l’instant, un commerçant qui a trop de Val’heureux en caisse et qui ne sait pas comment les faire circuler peut demander à l’ASBL le Val’heureux de lui reconvertir gratuitement ses V contre des €. Plus tard, quand davantage de boucles économiques seront établies et que les commerçants auront des possibilités de payer leurs fournisseurs en V, l’assemblée générale, dont les commerçants partenaires font partie, pourra éventuellement décider de faire payer des frais de change (uniquement pour des reconversions de Val’heureux en euros) pour décourager les commerçants de faire sortir des V du système. C’est l’assemblée générale qui pourra aussi décider également d’éventuelles exceptions pour certains secteurs (exemple, les bijouteries, parce que les bijoutiers ont peu de fournisseurs locaux – il faudra néanmoins examiner les autres possibilités de faire circuler le V).</p>

    <p>Aujourd’hui, le Val’heureux est toujours dans sa phase de démarrage (lancer une monnaie citoyenne et reconstruire un système économique local prend du temps !) et il n’y a encore que peu de producteurs dans le système, donc aucun frais de reconversion n’est envisagé. De plus, une version « électronique » du Val’heureux est en chantier (PC banking, smartphone et SMS). Il permettra d’encore mieux faire circuler la monnaie. Des verrous seront levés. La question des frais sera alors peut-être posée à l’assemblée générale.</p>

    <h3>J’ai peur d’être submergé de Val’heureux !</h3>

    <p>Le V est lancé depuis juin 2014 et les commerçants ne se plaignent pas. De temps en temps, des commerçants regrettent de ne pas voir davantage de gens payer en Val’heureux. C’est normal. Le V est toujours dans sa phase de démarrage. En comparaison avec la taille de l’économie régionale, il y a encore peu de V en circulation. Aujourd’hui, l’important est de créer le réseau et de soutenir la mise en place d’une économie plus durable qui soutient les commerces de proximité et les petits producteurs régionaux. Quand viendra le moment où plus de V seront dépensés dans les commerces, il y aura aussi plus de possibilités de les dépenser chez des fournisseurs, et plus de clients voudront aussi récupérer des V. À ce moment, l’afflux de V ne sera donc pas un problème. Au contraire, le V prendra tout son sens.</p>

    <h3>Ai-je mon mot à dire ?</h3>

    <p>Bien entendu. Le V est un outil économique au service de l’économie locale et du développement durable mais est aussi et surtout un outil au service de la souveraineté économique. Autrement dit, le V est une manière pour les citoyens et les commerçants de se réapproprier la gestion de l’économie locale. Les promoteurs du V souhaitent qu’un maximum de commerçants et de producteurs partenaires et d’utilisateurs s’investissent dans la réflexion et le développement du V. Les commerçants partenaires font partie de l’assemblée générale, pourront, s’ils le désirent, être élus au conseil d’administration, et peuvent apporter toutes les réflexions et critiques et proposer toutes les améliorations qu’ils souhaiteraient. Le V fonctionnera encore mieux si les commerçants et les citoyens se l’approprient.</p>

    <h3>Quels sont les avantages pour le commerçant ?</h3>

    <ul>
        <li>Visibilité et promotion faite par l’ASBL « le Val’heureux », notamment via le répertoire des prestataires partenaires, les flyers, le site, la page Facebook, les passages dans la presse et via les événements auxquels l’ASBL participe ou organisés par elle ;</li>
        <li>affirmation de l’identité (développement durable et local) et différenciation par rapport à la grande distribution ;</li>
        <li>fidélisation de la clientèle ;</li>
        <li><p>possibilité de simplification des systèmes de « cartes de fidélité » ;</p>
        Notons qu’il serait intéressant que les commerçants, au lieu de faire une ristourne pour remercier la fidélité de leur client, rendent l’équivalent de cette ristourne en Val’heureux. C’est bon pour le client, qui bénéficie toujours d’un cadeau, c’est bon pour le système Val’heureux puisque ça stimule la circulation de la monnaie et, enfin, c’est bon pour le commerçant qui ne perd pas, purement et simplement, le montant de sa ristourne puisque les Val’heureux offerts par le commerçant, ou par un autre membre du système, vont revenir au commerçant.</li>
        <li>intégration à un réseau de clients partagés avec les autres partenaires du système et un élargissement de la clientèle ;</li>
        <li>mise en réseau avec d’autres partenaires (dont des producteurs ou des fournisseurs) qui privilégient les objectifs du Val’heureux ;</li>
        <li>le Val’heureux a pour objectif de dynamiser le commerce local et donc de soutenir les petits entrepreneurs ;</li>
        <li>profiter des actions lancées par l’ASBL le Val’heureux comme par exemple l’enveloppe cadeau lors des fêtes de fin d’année (V dépensés dans le réseau, évidemment !).</li>
    </ul>

    <h2 id="comment-les-citoyens-peuvent-soutenir-le-projet">Comment les citoyens peuvent soutenir le projet ?</h2>

    <h3>En devenant membre</h3>

    <p>Parler du Val’heureux, dépenser en val’heureux et devenir membre de l’ASBL, c’est déjà participer concrètement à la construction d’une société plus équitable et plus respectueuse de l’environnement, mais c’est aussi exercer son pouvoir citoyen !</p>

    <h3>En prenant une part active</h3>

    <p>Vu le succès grandissant du Val’heureux, l’ASBL cherche activement des citoyens volontaires (aide administrative, communication, rencontre des partenaires, représentation, etc). N’hésitez pas à nous rejoindre !</p>
