    <h2 id="les-origines-du-val-heureux">Les origines du Val’heureux</h2>

    <h3>Origine du projet</h3>

    <p>L’idée de créer un bon de soutien à l’économie locale de la région liégeoise est née en 2011 à l’initiative de quelques citoyens actifs au sein de Liège en Transition ou liés au réseau Financité. Leur but : favoriser l’économie réelle, locale et éthique.</p>

    <p>Ce groupe d’une dizaine de personnes a élaboré son projet dans le cadre des actions menées par l’ASBL Barricade, par Liège en Transition et par les promoteurs de la Ceinture aliment-terre liégeoise (<a href="https://www.catl.be">www.catl.be</a>). Début 2014, une ASBL a été fondée et, en juin de la même année, les premiers billets de Valeureux sont apparus dans les portefeuilles de la Cité ardente.</p>

    <p>Début 2017, différents groupes de citoyen·ne·s de la province de Liège en réflexion sur l’économie locale et les circuits courts ont commencé à s’intéresser à la monnaie citoyenne. Ces groupes sont issus d’initiatives de transition (comme Comblain-au-Pont en Transition), de dynamiques de jeunes citoyens (Génér’action de Huy-Waremme) et d’associations comme De Bouche à Oreille (<a href="http://www.dbao.be">www.dbao.be</a>). Après quelques soirées de formation et de réflexion, ces différents groupes ont décidé d’unir leurs efforts pour travailler sur une nouvelle version de la monnaie citoyenne liégeoise. Ainsi, le Valeureux évoluait en Val’heureux, pour mieux soutenir les circuits courts d’une bonne partie de la province de Liège.</p>

    <p>Ce sont donc désormais cinq comités de citoyen·ne·s, rassemblés au sein de l’ASBL le Val’heureux, qui assurent la promotion et la gestion du Val’heureux. Ces groupes sont répartis dans les régions de Liège, Huy-Hesbaye-Condroz, Ourthe-Amblève, Verviers et Pays de Herve.</p>

    <h3>Contexte du projet</h3>

    <p>Le Val’heureux s’inscrit dans le vaste mouvement planétaire des « monnaies complémentaires ». On estime qu’il en existe aujourd’hui plus de 5000 dans le monde. En Belgique francophone, il y a, actuellement, six autres « monnaies complémentaires » en circulation : l’Épi lorrain à Virton, les Blés à Grez-Doiceau, le Talent à Ottignies/Louvain-La-Neuve, le Voltí à Rocherfort-Ciney-Marche, le Lumsou à Namur et le Sous-rire à Malmédy et le Ropi à Mons. D’autres projets sont également en voie de construction ailleurs en Wallonie et à Bruxelles. Enfin, le Toreke, soutenu par la ville de Gand, est, pour les promoteurs du Val’heureux, un modèle très inspirant.</p>

    <p>Certain·e·s pourraient être surpris·es qu’une poignée de citoyens prétendent créer une « monnaie ». En effet, l’émission de la « monnaie » est une prérogative aujourd’hui réservée à la Banque Centrale européenne et à la Banque nationale de Belgique. Ce que nous appelons ici « monnaie » est donc, d’un point de vue légal, un « bon de soutien à l’économie locale ». Cette mention figure sur tous les documents officiels.</p>

    <p>Si les promoteurs et promotrices du Val’heureux s’investissent dans la création et la gestion d’un bon de soutien à l’économie locale, c’est parce que, comme Bernard Lietaer (économiste et ancien haut fonctionnaire de la Banque nationale de Belgique), l’écrit, <em>« ils sont convaincus que l’euro ne suffit pas ! Il manque un outil économique destiné à stimuler exclusivement les échanges locaux, entièrement dédié au service de l’économie réelle, capable de renforcer l’emploi non délocalisable »</em>. En résumé, sa finalité consiste à orienter les comportements de consommation, soutenir les petits entrepreneurs et rapprocher les acteurs de l’économie locale pour répondre aux enjeux d’un développement plus durable et solidaire.</p>

    <p>La région liégeoise compte de nombreux entrepreneurs qui nourrissent le tissu économique local. Ils et elles travaillent au quotidien dans l’esprit d’un développement économique respectueux des personnes et de l’environnement. L’objectif du Val’heureux est donc de renforcer ces projets et leur clientèle.</p>

    <p>Pour ses promoteurs et promotrices, le Val’heureux constitue donc une alternative à la spirale infernale d’une certaine mondialisation économique qui déstructure sans cesse les économies locales, accapare les richesses, distend les liens sociaux et détruit l’environnement, source de toute prospérité.</p>

    <p>Enfin, via sa structure en ASBL, le Val’heureux est un outil transparent et démocratique, fruit d’une gestion citoyenne et participative.</p>

    <h2 id="structure-du-projet">Structure du projet</h2>

    <h3>Objectifs du Val’heureux</h3>

    <p>Le Val’heureux est un complément régional à l’euro. Le simple fait de transformer un euro en Val’heureux crée de la richesse qui circule dans un territoire donné, celui des bassins de vie en région liégeoise. Dès lors, le Val’heureux poursuit plusieurs objectifs :</p>

    <ul>
        <li>renforcer et valoriser l’économie locale et les circuits courts ;</li>
        <li>favoriser l’utilisation de biens et de services socialement responsables ;</li>
        <li>soutenir les initiatives respectueuses de l’environnement ;</li>
        <li>promouvoir en particulier la souveraineté alimentaire et économique ;</li>
        <li>créer du lien social sur base locale tout en facilitant les échanges.</li>
    </ul>

    <p>Ainsi, le Val’heureux permet de retenir et de mieux faire circuler la richesse créée par les petits entrepreneurs, les citoyens et les acteurs de l’économie locale et éthique.</p>

    <h3>Le Val’heureux en pratique</h3>

    <p>Le Val’heureux est un bon de soutien à l’économie locale pour les bassins de vie de la région liégeoise. Il est partagé par un réseau limité d’utilisateurs. La parité entre l’euro et le Val’heureux est fixe : 1 Val’heureux = 1 euro. Le Val’heureux se présente sous la forme de bons sécurisés imprimés sur papier. Il y a six coupures de billets, respectivement d’une valeur de 0 (zéro), 0,5 (un demi), 1 (un), 5 (cinq), 10 (dix) et 20 (vingt) Val’heureux.</p>

    <p>Ces billets ont été créés par la graphiste Colienne Roberti. Les promoteurs du Val’heureux n’ont rien prévu pour l’utilisation de la coupure de zéro Val’heureux, pariant sur l’imagination et l’ingéniosité de leurs concitoyen·ne·s pour en faire bon usage.</p>

    <p>Chaque coupure est liée à un thème :</p>

    <ul>
        <li>le lien social pour le billet de zéro ;</li>
        <li>l’environnement pour le billet d’un demi ;</li>
        <li>la culture pour le billet de un ;</li>
        <li>la souveraineté alimentaire pour le billet de cinq ;</li>
        <li>la liberté et la démocratie pour le billet de dix ;</li>
        <li>la solidarité pour le billet de vingt.</li>
    </ul>

    <p>Chaque coupure possède un recto unique et un verso qui, lui, est spécifique à chaque région (Liège, Huy-Hesbaye-Condroz, Verviers, Ourthe-Amblève et Pays de Herve). Ces versos reprennent un paysage, un monument ou un symbole local. Il y a donc trente billets différents (6 coupures, chacune avec 5 faces régionales différentes), dont la cohérence est assurée par une même charte graphique et qui peuvent circuler indifféremment partout où le Val’heureux est accepté.</p>

    <h3>Les acteurs du système</h3>

    <p>Toute personne physique ou morale peut utiliser des Val’heureux. Deux rôles différents peuvent être joués : celui d’« utilisateur·trice », ou celui de « prestataire partenaire ».</p>

    <h4>L’utilisateur·trice</h4>

    <p>Est utilisatrice toute personne qui participe à une transaction commerciale en Val’heureux. Elle peut devenir « membre adhérent » de l’ASBL pour marquer son soutien au système. En posant ce choix, elle s’identifie et est répertoriée dans un registre.</p>

    <p>Les « membres adhérents » peuvent devenir « membres effectifs » de l’ASBL si leur demande est acceptée et s’ils s’acquittent d’une cotisation dont le montant est fixé par l’assemblée générale sur proposition du conseil d’administration.</p>

    <h4>Le prestataire partenaire</h4>

    <p>Les prestataires partenaires sont des indépendants, des entreprises, des associations ou toute autre organisation qui vendent un bien ou un service et qui acceptent le Val’heureux comme moyen de paiement. Ils ont signé la charte du Val’heureux et adhèrent donc à ses principes. Tous les prestataires partenaires sont membres effectifs de l’assemblée générale de l’ASBL « le Val’heureux », où leurs idées, leurs avis et leurs voix sont, bien entendu, pris en compte. Les prestataires partenaires s’acquittent d’une cotisation annuelle fixée au minimum à 25€.</p>

    <h3>Dépenser les Val’heureux</h3>

    <p>Les Val’heureux peuvent être dépensés chez tous les prestataires partenaires. Ceux-ci figurent dans l’annuaire du Val’heureux publié notamment sur son site web. Durant les événements partenaires, le Val’heureux peut être utilisé en lieu et place des tickets.</p>

    <p>Lors d’un paiement en Val’heureux, la monnaie est rendue préférentiellement en Val’heureux. Pour la petite monnaie (cents) ou lorsque les coupures nécessaires ne sont pas disponibles, le change est rendu en euros.</p>

    <p>Lorsqu’il compte sa caisse en fin de journée, le prestataire partenaire comptabilise les euros et les Val’heureux en les intégrant indifféremment à son chiffre d’affaires.</p>

    <h3>Principe de gestion de l’ASBL « Le Val’heureux »</h3>

    <p>Les euros convertis en Val’heureux sont versés sur une réserve de contrepartie (un compte ouvert auprès de la banque Triodos), qui ne sert que dans ce but. La gestion quotidienne et les frais de fonctionnement de l’ASBL sont financés par un compte bancaire séparé.</p>

    <h3>Secteurs d’activité concernés par le Val’heureux</h3>

    <p>Le Val’heureux s’applique à tous les secteurs d’activité. Toutefois, la priorité est donnée au secteur alimentaire.</p>

    <p>La zone géographique d’action du Val’heureux inclut Liège, Verviers, Huy, la Hesbaye, le Condroz, Ourthe-Amblève et le Pays de Herve. Le Val’heureux tient compte des boucles économiques locales et de l’évolution de la Ceinture aliment-terre liégeoise (<a href="https://www.catl.be">www.catl.be</a>), en voie de construction.</p>

    <h3>L’ASBL « Le Val’heureux »</h3>

    <p>Durant le premier trimestre de l’année 2014, les statuts de l’ASBL ont été rédigés. Ils ont été validés par le tribunal de commerce de Liège le 19 mars 2014. Les statuts ont été adaptés en octobre 2017. Ils sont disponibles notamment via le site web du Val’heureux (<a href="https://valheureux.be">valheureux.be</a>).</p>

    <h3>Respect des objectifs et promotion du dispositif</h3>

    <p>L’ASBL « le Val’heureux » gère et développe le système mis en place. Elle veille au respect de la finalité sociale et aux objectifs du projet. Elle promeut l’utilisation des Val’heureux et encourage l’adhésion de nouveaux prestataires partenaires. Elle assure la promotion du système auprès du grand public et des commerçants. Elle encourage l’utilisation des Val’heureux tout au long des filières.</p>

    <p>Outre les prestataires partenaires, sont membres effectifs de l’ASBL, les particuliers dont la demande d’adhésion, soumise à l’assemblée générale par le conseil d’administration, a été acceptée.</p>

    <h3>Partenariat avec le réseau « Financité »</h3>

    <p>L’ASBL « le Val’heureux » a signé une convention de partenariat avec le réseau « Financité ». Son statut de « groupe local » lui permet de rester en contact et de développer des collaborations avec les autres groupes actifs dans le développement d’une monnaie citoyenne en Wallonie, à Bruxelles et également au niveau international. L’ASBL « le Val’heureux » profite également d’un soutien logistique et humain mis à disposition par Financité. Grâce aux heures d’éducation permanente réalisées par les volontaires du Val’heureux et valorisées par Financité, l’ASBL bénéficie également de quelques moyens financiers via le réseau.</p>

